#!/usr/bin/env node
/**
 * Writes mobile/src/devLanHost.ts from this Mac's primary IPv4 (en0–en4).
 * Invoked from postinstall and before Metro start so JS API base URL matches the phone-reachable host.
 */
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const FALLBACK = "192.168.146.215";

function detectLanIPv4() {
  for (const iface of ["en0", "en1", "en2", "en3", "en4"]) {
    try {
      const ip = execSync(`ipconfig getifaddr ${iface}`, { encoding: "utf8" }).trim();
      if (/^\d{1,3}(\.\d{1,3}){3}$/.test(ip)) {
        return ip;
      }
    } catch {
      /* try next interface */
    }
  }
  return FALLBACK;
}

const repoRoot = path.resolve(__dirname, "..");
const outFile = path.join(repoRoot, "mobile", "src", "devLanHost.ts");
const host = detectLanIPv4();
const body = `/**
 * Auto-generated by scripts/write-orzo-dev-host.cjs (npm start / postinstall).
 * Do not edit by hand.
 */
export const ORZO_LAN_HOST = ${JSON.stringify(host)} as const;
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, body);
console.log("[Orzo] dev LAN host:", host);
